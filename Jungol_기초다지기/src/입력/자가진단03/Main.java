package 입력.자가진단03;

public class Main {

	public static void main(String[] args) {
		/*두 개의 정수형 변수를 선언하고 값을 대입하여 아래와 같이 출력되는 프로그램을 작성하라.


		출력 예
		55 - 10 = 45
		2008 - 1999 = 9
		*/
		
		int i = 55;
		int i2 = 10;
		
		System.out.println(i + " - " + i2 + " = " + (i-i2));
	
		int i3 = 2008;
		int i4 = 1999;
		
		System.out.println(i3 + " - " + i4 + " = " + (i3-i4));
		
	}

}
